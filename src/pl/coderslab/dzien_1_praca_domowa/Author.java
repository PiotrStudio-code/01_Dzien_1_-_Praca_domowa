package pl.coderslab.dzien_1_praca_domowa;

public class Author {

   int id ;
   String firstName;
   String lastName ;
   String pseudonim;

    public Author(int id, String firstName, String lastName, String pseudonim) {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.pseudonim = pseudonim;
    }
}
